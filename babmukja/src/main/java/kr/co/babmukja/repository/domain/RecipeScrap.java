package kr.co.babmukja.repository.domain;

public class RecipeScrap {
	private int recipeScrapNo;
	private int scrapNo;
	private int recipeNo;
	private int memNo;
	private String scrapStatus;
	
	
	public int getRecipeScrapNo() {
		return recipeScrapNo;
	}
	public void setRecipeScrapNo(int recipeScrapNo) {
		this.recipeScrapNo = recipeScrapNo;
	}
	public int getScrapNo() {
		return scrapNo;
	}
	public void setScrapNo(int scrapNo) {
		this.scrapNo = scrapNo;
	}
	public int getRecipeNo() {
		return recipeNo;
	}
	public void setRecipeNo(int recipeNo) {
		this.recipeNo = recipeNo;
	}
	public int getMemNo() {
		return memNo;
	}
	public void setMemNo(int memNo) {
		this.memNo = memNo;
	}
	public String getScrapStatus() {
		return scrapStatus;
	}
	public void setScrapStatus(String scrapStatus) {
		this.scrapStatus = scrapStatus;
	}
	
	
}
